#![macro_use]
#![allow(unused)]

macro_rules! trace {
    ($s:literal $(, $x:expr)* $(,)?) => {
        {
            // defmt goes first because I believe it's a bit more reliable
            #[cfg(feature = "defmt")]
            ::defmt::trace!($s $(, $x)*);
            #[cfg(feature = "log")]
            ::log::trace!($s $(, $x)*);
            #[cfg(not(any(feature = "log", feature="defmt")))]
            let _ = ($( & $x ),*);
        }
    };
}

